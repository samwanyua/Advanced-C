#include <stdio.h>

int main(void)
{

//For loop = repeats a section of a code for a limited amount of times

    for (int i= 10; i >=1; i--) // i is the short form for index. 3 statements: counter or index( i = 1;), condition(i <= 10;), increment/decrement (i++)
    {
        printf("%d\n",i);
    }

    return 0;
}